API_DESCRIPTION = """
Документация REST API системы для генерации флеш-карточек Anki
с использованием RAG (Retrieval-Augmented Generation).

## Основные возможности

*   **Аутентификация:** JWT (Access & Refresh токены) с поддержкой OAuth2.
*   **Генерация карточек:** Автоматическое создание флеш-карточек
    на основе темы или текста с использованием LLM.
*   **RAG-система:** Индексация документов и контекстный поиск.
*   **Управление колодами:** Создание, редактирование и организация колод.
*   **Шаблоны:** Настраиваемые шаблоны для различных типов карточек.
*   **Синхронизация с Anki:** Экспорт и синхронизация через AnkiConnect.
*   **Чат с ИИ:** Интерактивное общение с ассистентом для работы с карточками.

## Формат работы

Все запросы к защищенным методам требуют заголовок:
`Authorization: Bearer <ваш_токен>`

## API

Все эндпоинты доступны по префиксу `/api/`.
"""

